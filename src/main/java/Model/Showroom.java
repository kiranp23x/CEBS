package Model;

public class Showroom {
    private int roomID;
    private int numSeats;

    public int getRoomID() {
        return roomID;
    }
    public int getNumSeats() {
        return numSeats;
    }

    public void setRoomID(int roomID) {
        this.roomID = roomID;
    }

    public void setNumSeats(int numSeats) {
        this.numSeats = numSeats;
    }
}
